CREATE TABLE PAIS (
    ID_PAIS NUMBER(3) NOT NULL,
    NOME VARCHAR2(250) NOT NULL,
    CONSTRAINT PK_PAIS PRIMARY KEY (ID_PAIS)
);

CREATE TABLE ESTADO (
    ID_ESTADO NUMBER(3) NOT NULL,
    ID_PAIS NUMBER(3) NOT NULL,
    NOME VARCHAR2(250) NOT NULL,
    
    CONSTRAINT PK_ESTADO PRIMARY KEY (ID_ESTADO),
    CONSTRAINT FK_ESTADO_PAIS FOREIGN KEY (ID_PAIS)
        REFERENCES PAIS(ID_PAIS)
);

CREATE TABLE CIDADE (
    ID_CIDADE NUMBER(3) NOT NULL,
    ID_ESTADO NUMBER(3) NOT NULL,
    NOME VARCHAR2(250) NOT NULL,

    CONSTRAINT PK_CIDADE PRIMARY KEY (ID_CIDADE),
    CONSTRAINT FK_CIDADE_ESTADO FOREIGN KEY (ID_ESTADO)
        REFERENCES ESTADO(ID_ESTADO)
);

CREATE TABLE BAIRRO (
    ID_BAIRRO NUMBER(3) NOT NULL,
    ID_CIDADE NUMBER(3) NOT NULL,
    ID_ESTADO NUMBER(3) NOT NULL,
    NOME VARCHAR2(250) NOT NULL,

    CONSTRAINT PK_BAIRRO PRIMARY KEY (ID_BAIRRO),
    CONSTRAINT FK_BAIRRO_CIDADE FOREIGN KEY (ID_CIDADE)
        REFERENCES CIDADE(ID_CIDADE),
    CONSTRAINT FK_BAIRRO_ESTADO FOREIGN KEY (ID_ESTADO)
        REFERENCES ESTADO(ID_ESTADO)
);

CREATE TABLE ENDERECO (
    ID_ENDERECO NUMBER(3) NOT NULL,
    ID_BAIRRO NUMBER(3) NOT NULL,
    ID_CIDADE NUMBER(3) NOT NULL,
    LOGRADOURO VARCHAR2(250) NOT NULL,
    NUMERO NUMBER(6) NOT NULL,
    COMPLEMENTO VARCHAR2(100),
    CEP CHAR(8) NOT NULL,

    CONSTRAINT PK_ENDERECO PRIMARY KEY (ID_ENDERECO),
    CONSTRAINT FK_ENDERECO_BAIRRO FOREIGN KEY (ID_BAIRRO)
        REFERENCES BAIRRO(ID_BAIRRO),
    CONSTRAINT FK_ENDERECO_CIDADE FOREIGN KEY (ID_CIDADE)
        REFERENCES CIDADE(ID_CIDADE)
);

